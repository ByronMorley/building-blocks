    <h1>Coastal destinations</h1>
    <div class="block text-block">
    
		<div class="pane round content typography">
            
            
            <p>In the United Kingdom, coastal areas have been important tourism destinations since Victorian times, when it became fashionable to bathe in the sea waters. This was thought at the time to have a number of health benefits.  Most of the coastal resorts in the United Kingdom can trace their origins back to the Victorian era.  These resorts also catered for workers from industrial cities who visited these towns for day trips and then for longer holidays.</p><p>Brighton in West Sussex and Bournemouth in Dorset were amongst the first coastal towns to develop as tourism destinations, along with Blackpool in the north-west of England and Scarborough on the Yorkshire coast.  These resorts have been tourism destinations for over 200 years. </p><p>Apart from the major coastal resorts, many towns and villages along the coast of the United Kingdom have developed into seaside resorts.  Examples of these include Tenby in south Wales, Llandudno in north Wales, Weston-Super-Mare in Somerset and Newquay in Cornwall.  In most cases, seaside towns such as those mentioned have a sandy beach as one of the main attractions.  Early tourists arrived by train, stayed in hotels and other accommodation close to the beach and spent a great deal of their time there.  More recently, tourists have become more demanding and are looking for a wider range of attractions, which have had to be provided by the resorts. </p><p>In the late 1930’s the Butlins holiday camps brought a new type of tourism to coastal resorts.  Today, Butlins holiday centres are still operating at three coastal resorts; Minehead, Skegness and Bognor Regis.</p><p>Away from the major coastal resorts and seaside towns, many other areas of the British coast are important tourism destinations.  A number of National Parks, including the Pembrokeshire Coast, North Yorkshire Moors and Exmoor include coastal areas within them.  In these areas, the main attraction is the spectacular scenery of cliffs, headlands and bays as well as the sandy beaches and small unspoilt settlements.  Other areas of coast, away from National Parks, are also important tourism destinations.  Examples would include the Gower Peninsula in south Wales, north Cornwall and the West Dorset coast, which is now a World Heritage Site.</p>
