    <h1>Natural attractions</h1>
    <div class="block text-block">
    
		<div class="pane round content typography">
            
            
            <p>Natural attractions are named features which appeal to tourists because of the nature of the landform or the beauty of the landscape in which the attraction is set.  Natural attractions include:</p><ul><li>Lakes</li>
<li>Rivers and landforms such as waterfalls and gorges</li>
<li>Caves</li>
<li>Mountains</li>
<li>Coastal features</li>
</ul><p>Some natural attractions are free to enter and for some there is an entry charge.  It is not possible to charge tourists to visit a mountain, but it may be more feasible to make a charge to visit a cave or waterfall, especially if this is on private land. </p><p>Usually, a range of tourist facilities are available at natural attractions which might consist of:</p><ul><li>An information centre or some form of information boards about the feature</li>
<li>Car parks and access for disabled visitors</li>
<li>Refreshment facilities</li>
<li>Souvenir shop</li>
<li>Toilet facilities</li>
<li>Guided tours and walks</li>
<li>Viewpoints</li>
</ul><p>Part of the appeal of many natural attractions is the facilities which are provided.  Many tourists appreciate the opportunity to find out some information about the attraction.  Most natural attractions are visited by tourists who are staying at destinations nearby and are making a half day or full day visit to the area in which the attraction is located. </p>
		</div>
    
</div>


<div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Lakes </h2>
            
            
            <p>Lakes add to the beauty and appeal of many landscapes and very often provide a range of tourist activities around their shores.  Travel and tourism organisations offer a range of water-based activities on many lakes and lakeside villages often provide tourist facilities.  Campsites and caravan sites close to lakes are popular because lake views are appealing to many tourists and a number of facilities are often nearby.   </p><p>The largest lake in Wales is Lake Bala (Llyn Tegid) which is approximately 6 kilometres long and 1.5 kilometres wide.  The town of Bala is situated at the northern end of the lake and narrow gauge Bala Lake Railway runs for several kilometres along its southern shore.  The lake is popular for all sorts of water-based activities including sailing, kayaking and windsurfing. White water rafting takes place on the River Dee which flows through the lake.</p>
		</div>
    
</div>


<div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Rivers  </h2>
            
            
            <p>Rivers add to the appeal of many landscapes and destinations.  The River Thames in London and many other rivers, provide tourist appeal.  Also, features such as gorges, canyons and waterfalls are attractive to tourists. </p>
		</div>
    
</div>


<div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Caves</h2>
            
            
            <p>The majority of cave systems have been produced by water running through cracks and fissures in limestone rock and causing weathering through chemical processes. This leads to the creation of underground systems of caves with a range of unique landform features.</p><p>In many areas cave systems have been developed as commercial operations and operate as attractions.  The appeal of caves is the opportunity to walk underground and experience a different environment.  Within the United Kingdom there are a number of cave systems, the most famous of which are the Cheddar Caves and the systems in the Castleton area of Derbyshire.</p>
		</div>
    
</div>


<div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Mountains</h2>
            
            
            <p>Many mountains would be seen as attractions although most ranges of mountains would be seen as a destination.  Examples of specific mountain attractions would be Ben Nevis in Scotland, Snowdon in Wales and Helvelyn in England.</p><p>Mountains attract and appeal to tourists for different reasons.  Some just like to gaze at the scale and beauty of the scenery, others see mountains as a challenge and want to climb them or ski down them. </p>
		</div>
    
</div>


<div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Coastal features</h2>
            
            
            <p>As indicated earlier, coastal areas provide a range of tourist appeal.  Every beach in the world could be classified as a natural attraction and many of these have a range of facilities provided for tourists.  For some tourists the facilities provided add to the appeal of the beach, while other tourists find deserted and isolated beaches more appealing.</p><p>Apart from beaches, there is a range of coastal landforms which provide appeal and interest for tourists.  Around the coast of Britain, features ranging from Durdle Door in Dorset, to Beachy Head in Sussex and the Giant’s Causeway in Northern Ireland are all specific attractions which have tourist appeal and interest.  At these, and many other coastal attractions, there is a range of facilities provided for tourists and part of the appeal is in walking along a cliff top to view the landform.  Cliffs, stacks, arches, caves and islands off the coast all provide tourist interest and appeal.</p>
		</div>
    
</div>


