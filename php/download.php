    <h1>Rural destinations</h1>
    <div class="block text-block">
    
		<div class="pane round content typography">
            
            
            <p>Many areas of countryside receive some visits from tourists.  However, throughout the United Kingdom there are a number of countryside areas which attract a large number of tourists, mainly because of the good scenery and the range of natural attractions such as lakes, rivers, mountains and hills found within these areas.</p><p>Very often, countryside areas contain attractive towns and villages as well as pretty scenery.  In popular areas these provide accommodation such as camp sites, small hotels and bed and breakfast establishments.  Cafes and restaurants for those visitors touring the area are also often provided. The farming landscape of fields, crops and animals can also form part of the appeal of certain areas. </p><p>Most areas of countryside which are identified as tourism destinations provide a range of activities for their visitors.  These activities include mountaineering, rock climbing, caving and abseiling.  Water-based activities including sailing on lakes and canoeing or rafting on rivers are also common.  Cycling and walking are other popular activities. </p><p>The appeal of Dartmoor, as in many National Parks, is added to by the farming landscape and picturesque villages.</p><p>Many people enjoy visiting countryside areas by car and it is generally the case that many of these visitors do not travel far from their vehicles. These visitors require car parking areas and picnic sites close to roads. This type of tourist activity is often referred to as ‘a view, a brew and a loo’!  </p><p>All National Parks within the United Kingdom are areas of countryside and are popular tourism destinations. </p><p>All National Parks must balance the need to conserve and protect the natural environment and scenery of the area with the needs of tourists who wish to appreciate and enjoy the landscape and special qualities of the parks.  Very often, the communities living within the parks depend very much on the income from tourism, and so the needs of the local people must also be considered.</p><p>Apart from National Parks, other countryside areas which are protected in some way, are also popular tourism destinations. Within the United Kingdom, Areas of Outstanding Natural Beauty (AONB) are areas like National Parks and contain special landscapes.  An example would be the Gower Peninsula in South Wales.  </p>
