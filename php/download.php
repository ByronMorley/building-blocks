		<div class="pane round content typography">
            
				<h2>built attractions</h2>
            
            
            <p>These are places which have been built specifically to attract tourists.  Theme parks, amusement parks, aquariums etc. are all examples of purpose-built attractions.  In most cases tourists will have to pay an entry charge since the attractions will be part of the private sector.</p>
		</div>
    
</div>


