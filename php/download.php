    <h1>Equalities</h1>
    <div class="block text-block">
    
		<div class="pane round content typography">
            
				<h2>Equalities</h2>
            
            
            <p>Tourism organisations must ensure that all their customers are treated equally and laws have been passed to make sure that all customers are treated the same.  The most recent law is called The Equalities Act.</p><p>However, there are many examples where some customers are unable to use all of the services and facilities which are provided by the organisation.  A good example is young children not being able to go on the ‘white-knuckle’ rides in theme parks.</p><p>Some tourism organisations produce what is known as an Access Statement which sets out how the organisation can help people who may have disabilities or who require special assistance.  It may also inform customers of issues that they might encounter when they visit the organisation.  This helps to ensure that all customers have a good experience with tourism organisations. </p>
		</div>
    
</div>


