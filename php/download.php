            <p>As you get older you will be able to make more choices about where you go on holiday, the attractions you visit and the tourism activities you take part in.</p><p>Hopefully you will be able to enjoy your holidays and have great fun visiting theme parks and other attractions.</p><p>But you will only be able to really enjoy yourself if a wide range of people working in the tourism industry do their jobs properly and provide you with help or ‘service’.&nbsp; These people include travel agents, hotel receptionists, ride operators, tour guides, resort reps and many more.&nbsp; Without their help, or if they don’t do their jobs well, your experience of the tourism industry may well be less that you expected.</p><p>That’s what this unit is all about.</p><p>The customer experience helps you to understand the background to how providing good customer service helps tourism organisations to give their customers a great experience and to be successful.</p><p>Of course, you may well find yourself working in the tourism industry in a few years’ time providing service to customers.&nbsp; Or you may be working in a wide range of other industries where you are providing service to customers in different situations.</p><p>In this first section of the unit you will be investigating some of the principles of customer service.&nbsp; When you have studied this section, you will be asked to write a report on how principles of customer service can be applied to a tourism organisation you have studied.</p>
